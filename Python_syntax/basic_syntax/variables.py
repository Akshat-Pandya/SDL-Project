# variable is the name given to a memory location used to store some data 
# during the program execution .

# Python has following datatypes - Integer, Floating point , String , Boolean , None
# Python is a dynamically typed language means we need not to specify the datatype it is decided 
# automatically at the runtime by the pvm.

# Variable naming rules - 
# 1. Can contain alphabets , digits , underscores 
# 2. Can only start with alphabet/underscore 
# 3. No whitespaces allowed

# Operators - Arithematic, Assignment , comparison (> , < , >= , <= , ==), 
# logical (and or not , && || ! , & |)
