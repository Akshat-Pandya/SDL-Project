# What is a DataFrame?
# A DataFrame is a 2-dimensional, tabular data structure in Pandas.
# It consists of labeled rows and columns, similar to a spreadsheet or SQL table.
# Each column can hold different data types (integers, strings, floats, etc.).
# It is widely used for data analysis and manipulation.
import pandas as pd

# Creating a DataFrame
data = {
    'Name': ['Alice', 'Bob', 'Charlie'],
    'Age': [25, 30, 35],
    'City': ['New York', 'Paris', 'London']
}

# Display the DataFrame
print(data," ")
